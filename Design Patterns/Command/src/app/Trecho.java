package app;


public class Trecho {

    private final int início;
    private final int término;
    
    public Trecho(int início, int término)
    {
        this.início = início;
        this.término = término;
    }
    
    public int getInício()
    {
        return início;
    }

    public int getTérmino()
    {
        return término;
    }
   


}
